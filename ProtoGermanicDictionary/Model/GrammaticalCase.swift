import Foundation

enum GrammaticalCase: String, CaseIterable {
    case nominative = "Nominative"
    case vocative = "Vocative"
    case accusative = "Accusative"
    case genitive = "Genitive"
    case dative = "Dative"
    case instrumental = "Instrumental"
    case possessive   = "Possessive"
}
